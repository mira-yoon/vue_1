<template>
	<div>
		<h1>템플릿 문법</h1>

		<h2>보간법</h2>
		<p>{{ message }}</p>
		<p v-once>{{ message }}</p>
		<button v-on:click="message = message + '!'">Click!</button>
		<hr />

		<h2>v-html</h2>
		<p>{{ rawHtml }}</p>
		<p v-html="rawHtml"></p>
		<hr />

		<h2>속성 바인딩</h2>
		<!-- <div v-bind:title="dynamicTitle">마우스를 올려보세요</div> -->
		<!-- v-bind는 아래와 같이 :만 써도 된다. -->
		<div :title="dynamicTitle">마우스를 올려보세요</div>
		<input type="text" value="홍길동" v-bind:disabled="isInputDisabled" />
		<input v-bind="attrs" />
		<hr />

		<h2>JavaScript</h2>
		<p>{{ message.split('') }}</p>
		<p>{{ message.split('').reverse() }}</p>
		<p>{{ message.split('').reverse().join('') }}</p>
		<p>{{ isInputDisabled ? '예' : '아니오' }}</p>
	</div>
</template>

<script>
import { ref } from 'vue';

export default {
	setup() {
		const message = ref('안녕하세요!');
		const rawHtml = ref('<strong>안녕하세요</strong>');
		const dynamicTitle = ref('안녕하세요~!');
		const isInputDisabled = ref(true);
		const attrs = ref({
			type: 'text',
			value: '12345678',
			disabled: false,
		});
		return {
			message,
			rawHtml,
			dynamicTitle,
			isInputDisabled,
			attrs,
		};
	},
};
</script>

